package com.example.she_is_a_girl;

public class NumberUpdatte {
    private String id;
    private  String Number;
    public NumberUpdatte()
    {

    }

    public NumberUpdatte(String id, String number) {
        this.id = id;
        Number = number;
    }

    public String getId() {
        return id;
    }

    public String getNumber() {
        return Number;
    }
}
