package com.example.she_is_a_girl.Models;

public class Tamim {
    private  String search,name,uid;

    public Tamim() {
    }

    public Tamim(String search, String name, String uid) {
        this.search = search;
        this.name = name;
        this.uid = uid;
    }

    public String getSearch() {
        return search;
    }

    public String getName() {
        return name;
    }

    public String getUid() {
        return uid;
    }
}
